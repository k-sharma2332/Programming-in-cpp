// WAP to find the smallest missing positive element in the sorted Array that contains only positive element.
#include <iostream>
using namespace std;

int main()
{
    int sub[50], i;
    for (i=0;i<=48;i++);
    {
        sub[i] = i;
        cout << sub[i] << endl;
    }
    return 0;
}

