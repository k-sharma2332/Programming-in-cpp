#include<iostream>
using namespace std;
int main(){
    int x = 9;
    int y = 8;
    cout<<x+y<<endl;
    cout<<x-y<<endl;
    cout<<x*y<<endl;
    cout<<x/y<<endl;

    // Increment Operator
    int a = 4;
    a++;
    cout<<a<<endl;
    a--;
    cout<<a<<endl;
    ++a;
    cout<<a<<endl;
    --a;
    cout<<a<<endl;
}